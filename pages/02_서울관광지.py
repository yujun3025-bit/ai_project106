# ---------------------------------------------------
# 컬러 지도 생성
# tiles 변경
# ---------------------------------------------------
seoul_map = folium.Map(
    location=[37.5665, 126.9780],
    zoom_start=11,
    tiles="CartoDB Voyager"
)

# ---------------------------------------------------
# 마커 추가
# ---------------------------------------------------
for idx, (name, info) in enumerate(tourist_spots.items(), start=1):

    popup_html = f"""
    <div style="width:220px">
        <h4>{idx}. {name}</h4>
        <p>{info['desc']}</p>
        <p><b>🚇 가까운 지하철:</b><br>{info['subway']}</p>
    </div>
    """

    folium.Marker(
        location=[info["lat"], info["lon"]],
        popup=folium.Popup(popup_html, max_width=300),
        tooltip=name,
        icon=folium.Icon(
            color="red",
            icon="star"
        )
    ).add_to(seoul_map)

# ---------------------------------------------------
# 지도 출력 (80% 크기 느낌)
# ---------------------------------------------------
col1, col2, col3 = st.columns([1, 8, 1])

with col2:
    st_folium(
        seoul_map,
        width=900,
        height=600
    )

# ---------------------------------------------------
# 관광지 선택
# ---------------------------------------------------
st.divider()

